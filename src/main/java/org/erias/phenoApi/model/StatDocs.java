package org.erias.phenoApi.model;

public class StatDocs {
	private long nbPat;
	private long nbDocs;
	private long nbConcept;
	/**
	 * @return the nbPat
	 */
	public long getNbPat() {
		return nbPat;
	}
	/**
	 * @param nbPat the nbPat to set
	 */
	public void setNbPat(long nbPat) {
		this.nbPat = nbPat;
	}
	/**
	 * @return the nbDocs
	 */
	public long getNbDocs() {
		return nbDocs;
	}
	/**
	 * @param nbDocs the nbDocs to set
	 */
	public void setNbDocs(long nbDocs) {
		this.nbDocs = nbDocs;
	}
	/**
	 * @return the nbConcept
	 */
	public long getNbConcept() {
		return nbConcept;
	}
	/**
	 * @param nbConcept the nbConcept to set
	 */
	public void setNbConcept(long nbConcept) {
		this.nbConcept = nbConcept;
	}
}
